public class CheckersBoard{
	
	public static void main(String[] args){
		
		int count = 0;
		
		while(count < 4){
			System.out.print("* * * * * * * * * * * * * * * * * * *");
			System.out.println("");
			System.out.print(" "+"* * * * * * * * * * * * * * * * * * * ");
			System.out.println("");
			count++;
		}
		
	}
	
}